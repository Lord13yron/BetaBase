CREATE OR REPLACE FUNCTION public.custom_access_token_hook(event jsonb)
 RETURNS jsonb
 LANGUAGE plpgsql
 STABLE
AS $function$
DECLARE
  claims jsonb;
  has_username boolean;
BEGIN
  SELECT (username IS NOT NULL AND username != '')
  INTO has_username
  FROM public.user_profiles
  WHERE id = (event->>'user_id')::uuid;

  claims := event->'claims';
  claims := jsonb_set(claims, '{has_username}', to_jsonb(COALESCE(has_username, false)));

  RETURN jsonb_set(event, '{claims}', claims);
END;
$function$
;

